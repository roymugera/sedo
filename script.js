function arrowUp () {
    document.getElementById('shadowBox').style.height = 'auto';
    document.getElementById('shadowBox').style.visibility = 'visible';
    document.getElementById('content0').style.visibility = 'visible';
}

function arrowDown () {
    document.getElementById('shadowBox').style.height = '0';
    document.getElementById('shadowBox').style.visibility = 'hidden';
    document.getElementById('content0').style.visibility = 'hidden';
}

